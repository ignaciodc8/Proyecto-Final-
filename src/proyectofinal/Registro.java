    private void botonRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegActionPerformed
        String userName = txtNombre.getText();
        String dni = txtDNI.getText();
        String password = txtPassField.getText();
        
        if (userName.isEmpty() || dni.isEmpty() || password.isEmpty()) {
        
            JOptionPane.showMessageDialog(null, "¡Hay campos vacíos!");
        } else {
            try {
            String query = "INSERT INTO usuarios (nombreusuario, documento, clave) VALUES ('"+userName+"', '"+dni+"', '"+password+"')";
            PreparedStatement ps = cn.prepareStatement(query);
            ps.executeUpdate();
            limpiar();
            JOptionPane.showMessageDialog(null, "Usted se ha registrado correctamente.");
            } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "No se pudo registrar el usuario. Intente nuevamente.");
            }
        }
        
        
    }//GEN-LAST:event_botonRegActionPerformed
        void limpiar() {
        txtNombre.setText(null);
        txtDNI.setText(null);
        txtPassField.setText(null);
        }
